import numpy as np

class Layer:
    def __init__(self, bias, weights):
        self.bias = bias
        self.weights = weights


def readLayersFromFile(filename):
    layers = []
    with open(filename, 'r') as file:
        numLayers = int(file.readline())
        
        for i in range(numLayers):
            # read bias
            biasSize = int(file.readline())
            bias = [float(x) for x in file.readline().split()]
            temp = []
            for i in bias:
                temp.append([i])
            bias = np.array(temp)
            
            # read weight
            rows, cols = map(int, file.readline().split())
            weights = []
            for j in range(rows):
                weights.append([float(x) for x in file.readline().split()])
            weights = np.array(weights)
            
            layers.append(Layer(bias, weights))
    
    return layers


def feedForward(layer, input): # ReLU(weights * input + bias)
    return np.dot(layer.weights, input) + layer.bias


def ReLU(input):
    for i in input:
        if i[0] < 0:
            i[0] = 0;
    return input


def forwardPropagate(layers, input):
    # reformat
    input = input.transpose()
    temp = []
    for row in input:
        for pixel in row:
            temp.append([pixel])
    for pixel in temp:
        pixel[0] = float(pixel[0]) / 255.0
    input = np.array(temp)
    
    # propagate
    for layer in layers:
        input = feedForward(layer, input)
        input = ReLU(input)
    return input


def getResult(output):
    maxIndex = 0;
    index = 0;
    max = 0;
    for i in output:
        if i[0] > max:
            maxIndex = index
            max = i[0]
        index += 1
    return maxIndex



